int	ft_atoi(const char *str)
{
	int i;
	int ch;
	int znak;

	i = 0;
	ch = 0;
	znak = 1;
	while ((str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r') || \
			(str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n'))
		i++;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			znak *= -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		ch = (ch * 10) + (str[i] - 48);
		i++;
	}
	return (znak * ch);
}
